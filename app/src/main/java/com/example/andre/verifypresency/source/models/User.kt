package com.example.andre.verifypresency.source.models

/**
 * Class which encapsulates user inputs.
 */
class User {

    /**
     * Contains user input text specified for "First Name" View
     */
    var firstName: String? = null

    /**
     * Contains user input text specified for "Last Name" View
     */
    var lastName: String? = null

    /**
     * Contains user input text specified for "Organization Name" View
     */
    var orgName: String? = null

    /**
     * Contains user input text specified for "Email" View
     */
    var email: String? = null

    /**
     * Contains user input text specified for "Password" View
     */
    var password: String? = null

    /**
     * Contains user input text specified for "Confirm Password" View
     */
    var confirmPassword: String? = null

}